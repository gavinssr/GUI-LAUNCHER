type NavbarIconName =
  | "search"
  | "searcher"
  | "cart"
  | "more"
  | "support"
  | "calendar"
  | "bag"

function NavbarGlyph({
  name,
  className,
}: {
  name: NavbarIconName
  className?: string
}) {
  const iconProps: React.ComponentProps<"svg"> = {
    className,
    "aria-hidden": true as const,
  }

  switch (name) {
    case "search":
      return <SearchFieldIcon {...iconProps} />
    case "searcher":
      return <SearcherIcon {...iconProps} />
    case "cart":
      return <CartIcon {...iconProps} />
    case "more":
      return <MoreIcon {...iconProps} />
    case "support":
      return <SupportIcon {...iconProps} />
    case "calendar":
      return <CalendarIcon {...iconProps} />
    case "bag":
      return <BagIcon {...iconProps} />
    default:
      return null
  }
}

